    private void SubCodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SubCodeKeyReleased
        // TODO add your handling code here:
        String pattern="^[0-9]{1,20}$";
        Pattern patt=Pattern.compile(pattern);
        Matcher match=patt.matcher(SubCode.getText());
        if(!match.matches())
        {
            valCode.setText("Ineger Number Only");
        }
        else
        {
               valCode.setText("");
        }
        
    }//GEN-LAST:event_SubCodeKeyReleased

