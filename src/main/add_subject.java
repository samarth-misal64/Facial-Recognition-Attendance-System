    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        // TODO add your handling code here:
          if(SubCode.getText().isEmpty()||SubName.getText().isEmpty()||Hours.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else
        {
        try
        {
            Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            Con = DriverManager.getConnection("jdbc:ucanaccess://attendance_db/face_recog_db.accdb");
           PreparedStatement add = Con.prepareStatement("insert into subject values(?,?,?,?)");
           add.setInt(1, Integer.valueOf(SubCode.getText()));
           add.setString(2, SubName.getText());
           add.setInt(3, Integer.valueOf(Hours.getText()));
           add.setString(4, CourseCB.getSelectedItem().toString());
          // add.setString(4, GenderCb.getSelectedItem().toString());
           int row = add.executeUpdate();
           JOptionPane.showMessageDialog(this, "Subject Added Succesfully");
           Con.close();
          SelectSubject();
         
           
        }catch(Exception e)
        {
           e.printStackTrace();
        }
        }
       
    }//GEN-LAST:event_saveMouseClicked

